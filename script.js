function drawLineChart(canvas, current, previous = null) {
  const ctx = canvas.getContext("2d");
  const ratio = window.devicePixelRatio || 1;
  const width = canvas.clientWidth;
  const height = canvas.clientHeight;
  canvas.width = width * ratio;
  canvas.height = height * ratio;
  ctx.scale(ratio, ratio);
  ctx.clearRect(0, 0, width, height);

  const allPoints = previous ? current.concat(previous) : current;
  const max = Math.max(...allPoints) + 6;
  const min = Math.min(...allPoints) - 4;
  const pad = 24;

  for (let i = 0; i < 4; i += 1) {
    const y = pad + ((height - pad * 2) / 3) * i;
    ctx.strokeStyle = getComputedStyle(document.body).getPropertyValue("--line");
    ctx.lineWidth = 1;
    ctx.beginPath();
    ctx.moveTo(pad, y);
    ctx.lineTo(width - pad, y);
    ctx.stroke();
  }

  const drawSeries = (series, color, fill = false) => {
    ctx.beginPath();
    series.forEach((point, index) => {
      const x = pad + (index * (width - pad * 2)) / (series.length - 1);
      const y = height - pad - ((point - min) / (max - min)) * (height - pad * 2);
      if (index === 0) {
        ctx.moveTo(x, y);
      } else {
        ctx.lineTo(x, y);
      }
    });
    ctx.strokeStyle = color;
    ctx.lineWidth = 3;
    ctx.stroke();

    if (fill) {
      ctx.lineTo(width - pad, height - pad);
      ctx.lineTo(pad, height - pad);
      ctx.closePath();
      const grad = ctx.createLinearGradient(0, pad, 0, height - pad);
      grad.addColorStop(0, color + "55");
      grad.addColorStop(1, color + "00");
      ctx.fillStyle = grad;
      ctx.fill();
    }
  };

  if (previous) {
    drawSeries(previous, "#9aa3b2");
  }
  drawSeries(current, getCSS("--accent-2"), true);
}

function drawSparkline(canvas, values) {
  const ctx = canvas.getContext("2d");
  const ratio = window.devicePixelRatio || 1;
  const width = canvas.clientWidth;
  const height = canvas.clientHeight;
  canvas.width = width * ratio;
  canvas.height = height * ratio;
  ctx.scale(ratio, ratio);
  ctx.clearRect(0, 0, width, height);

  const max = Math.max(...values);
  const min = Math.min(...values);
  const pad = 10;

  ctx.beginPath();
  values.forEach((value, index) => {
    const x = pad + (index * (width - pad * 2)) / (values.length - 1);
    const y = height - pad - ((value - min) / (max - min || 1)) * (height - pad * 2);
    if (index === 0) {
      ctx.moveTo(x, y);
    } else {
      ctx.lineTo(x, y);
    }
  });
  ctx.strokeStyle = getCSS("--accent");
  ctx.lineWidth = 3;
  ctx.stroke();

  const lastX = pad + ((values.length - 1) * (width - pad * 2)) / (values.length - 1);
  const lastY =
    height - pad - ((values[values.length - 1] - min) / (max - min || 1)) * (height - pad * 2);
  ctx.fillStyle = getCSS("--accent-2");
  ctx.beginPath();
  ctx.arc(lastX, lastY, 5, 0, Math.PI * 2);
  ctx.fill();
}

function getCSS(variable) {
  return getComputedStyle(document.body).getPropertyValue(variable).trim();
}

function updateDashboard() {
  const data = dashboardData[state.platform];
  $("#liveScore").textContent = data.liveScore;
  $("#engagementValue").textContent = `${data.engagementRate}%`;
  $("#engagementShift").textContent = data.engagementShift;
  $("#growthValue").textContent = data.totalFollowers;
  $("#growthShift").textContent = data.followerShift;
  $("#forecastTitle").textContent = data.forecastTitle;
  $("#forecastCopy").textContent = data.forecastCopy;
  $("#ringValue").textContent = `${data.ring}%`;
  $("#ringProgress").style.strokeDashoffset = `${100 - data.ring}`;

  createMiniBars(data.sparkBars);
  renderInsights();
  renderActions();
  drawSparkline($("#followersCanvas"), data.followerTrend);
  drawLineChart(
    $("#growthCanvas"),
    data.growthCurrent,
    state.growthCompare ? data.growthPrevious : null
  );
}

function initializeEvents() {
  $("#platformSelect").addEventListener("change", (event) => {
    state.platform = event.target.value;
    updateDashboard();
  });

  $("#themeToggle").addEventListener("click", () => {
    const isDark = document.body.classList.toggle("dark");
    localStorage.setItem("pulseboard-theme", isDark ? "dark" : "light");
    $("#themeToggle").setAttribute("aria-pressed", String(isDark));
    $(".toggle-text").textContent = isDark ? "Switch to Daylight" : "Switch to Night Shift";
    updateDashboard();
  });

  $("#growthMode").addEventListener("click", () => {
    state.growthCompare = !state.growthCompare;
    $("#growthMode").textContent = state.growthCompare
      ? "Hide previous week"
      : "Compare to previous week";
    updateDashboard();
  });

  $$(".insight-tab").forEach((button) => {
    button.addEventListener("click", () => {
      $$(".insight-tab").forEach((tab) => tab.classList.remove("active"));
      button.classList.add("active");
      state.insightSegment = button.dataset.segment;
      renderInsights();
    });
  });

  $$(".filter-chip").forEach((button) => {
    button.addEventListener("click", () => {
      $$(".filter-chip").forEach((chip) => chip.classList.remove("active"));
      button.classList.add("active");
      state.contentFilter = button.dataset.filter;
      renderContent();
    });
  });

  $("#scheduleList").addEventListener("click", (event) => {
    const card = event.target.closest(".schedule-card");
    if (!card) {
      return;
    }
    const post = scheduledPosts[Number(card.dataset.index)];
    post.live = !post.live;
    post.status = post.live ? "Green room confirmed" : "Needs final review";
    renderSchedule();
  });

  $$(".nav-pill").forEach((button) => {
    button.addEventListener("click", () => {
      $$(".nav-pill").forEach((pill) => pill.classList.remove("active"));
      button.classList.add("active");
      document.getElementById(button.dataset.target).scrollIntoView({ behavior: "smooth" });
    });
  });

  window.addEventListener("resize", updateDashboard);
}

function initializeTheme() {
  const savedTheme = localStorage.getItem("pulseboard-theme");
  if (savedTheme === "dark") {
    document.body.classList.add("dark");
    $("#themeToggle").setAttribute("aria-pressed", "true");
    $(".toggle-text").textContent = "Switch to Daylight";
  }
}

initializeTheme();
renderSchedule();
renderContent();
initializeEvents();
updateDashboard();
