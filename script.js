const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => document.querySelectorAll(selector);

function createMiniBars(values) {
  const wrapper = $("#engagementBars");
  wrapper.innerHTML = "";
  values.forEach((value, index) => {
    const bar = document.createElement("span");
    bar.style.height = `${value}%`;
    bar.style.animationDelay = `${index * 40}ms`;
    wrapper.appendChild(bar);
  });
}

function renderInsights() {
  const dataset = dashboardData[state.platform].insights[state.insightSegment];
  $("#audienceLabel").textContent = dataset.label;
  const wrap = $("#insightBars");
  wrap.innerHTML = "";

  dataset.rows.forEach(([label, value]) => {
    const row = document.createElement("div");
    row.className = "insight-row";
    row.innerHTML = `
      <span>${label}</span>
      <div class="insight-track"><div class="insight-fill" style="width:${value}%"></div></div>
      <strong>${value}%</strong>
    `;
    wrap.appendChild(row);
  });
}

